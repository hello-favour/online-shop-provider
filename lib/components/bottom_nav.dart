import 'package:flutter/material.dart';

class BottomNavWidget extends StatelessWidget {
  final IconData icon;
  final VoidCallback onTap;
  const BottomNavWidget({
    Key? key,
    required this.icon,
    required this.onTap,
  }) : super(key: key);

  @override
  Widget build(BuildContext context) {
    return GestureDetector(
      onTap: onTap,
      child: SizedBox(
        height: 36,
        width: 36,
        child: Icon(
          icon,
          color: Colors.white,
        ),
      ),
    );
  }
}
